function imageKind(name: string, groupName = ''): keyof typeof imagePools {
  const text = `${name}${groupName}`
  if (/塔可|莎莎|墨西哥|卷饼|饭碗|玉米片|鳄梨/.test(text)) return 'taco'
  if (/汉堡|堡|薯条/.test(text)) return 'burger'
  if (/烧烤|烤串|小串|生蚝|鱿鱼|锡纸|烤冷面|烤馒头/.test(text)) return 'bbq'
  if (/炸鸡|鸡桶|鸡腿|鸡翅|鸡块|鸡排|原味鸡/.test(text)) return 'chicken'
  if (/披萨|意面|焗/.test(text)) return 'pizza'
  if (/咖啡|拿铁|美式|摩卡|冷萃/.test(text)) return 'coffee'
  if (/奶茶|果茶|乌龙|茉莉|茶|椰|葡萄|芒|饮|豆浆|可乐|气泡|酸梅|冰峰/.test(text)) return 'tea'
  if (/饭|盖饭|炒蛋|鸡汤|排骨|牛柳|扣肉|便当/.test(text)) return 'rice'
  if (/面|粉|馄饨|云吞|蒸饺|拌面|炒面/.test(text)) return 'noodle'
  if (/冒菜|麻辣烫|火锅|鸭血|肥牛|鱼片/.test(text)) return 'hotpot'
  if (/卤|鸭|豆干|藕|小吃|薯角|酥肉|鸡米|土豆泥|玉米/.test(text)) return 'snack'
  if (/甜|蛋糕|布丁|蛋挞|麦芬|布朗尼|麻薯|米糕|冰粉/.test(text)) return 'dessert'
  if (/寿司|刺身|饭团|鳗鱼|玉子|海苔/.test(text)) return 'sushi'
  if (/沙拉|轻食|藜麦|牛油果|能量碗|糙米|鹰嘴豆/.test(text)) return 'salad'
  if (/烘焙|面包|可颂|法棍|肉松|吐司|红茶蛋糕|奶油卷|黑森林/.test(text)) return 'bakery'
  if (/水果|果|西瓜|苹果|香蕉|橙|花束|玫瑰|鲜切|果汁/.test(text)) return 'fruit'
  if (/药|贴|维生素|口罩|棉签|碘伏|钙片|菌|眼罩|润喉|消毒/.test(text)) return 'medicine'
  if (/超市|鲜食|乳品|零食|日用|蔬菜|肉禽|海鲜|熟食|生菜|番茄|鸡胸|虾/.test(text)) return 'market'
  return 'rice'
}

function foodImage(name: string, groupName: string, index: number) {
  const pool = imagePools[imageKind(name, groupName)]
  return pool[index % pool.length]
}

function foodPrice(name: string, groupName: string, index: number) {
  const band = priceBands[imageKind(name, groupName)]
  return band[index % band.length]
}

function extraFor(groupName: string, names: readonly string[]) {
  const kind = imageKind(names.join(''), groupName)
  return extraDishes[kind].filter(name => !names.includes(name)).slice(0, 5)
}

